# 색상 — 편집 기준 (검정 outline + 흰색 fill).
# 사용자가 직접 색 변경하려면 SVG 파일의 fill="#000000" / fill="#FFFFFF" 수정.
OUTLINE_COLOR = "#000000"
FILL_COLOR    = "#FFFFFF"


def make_svg(fill_d: str, outline_d: str) -> str:
    """duotone 구조 (fill + outline) SVG 생성."""
    return (
        '<svg xmlns="http://www.w3.org/2000/svg" '
        'width="256" height="256" viewBox="0 0 256 256">\n'
        f'  <path d="{fill_d}" fill="{FILL_COLOR}"/>\n'
        f'  <path d="{outline_d}" fill="{OUTLINE_COLOR}"/>\n'
        '</svg>\n'
    )


def main():
    os.makedirs(SVG_DIR, exist_ok=True)
    os.makedirs(PNG_DIR, exist_ok=True)

    for name, paths in ICONS.items():
        svg_text = make_svg(paths["fill"], paths["outline"])

        # SVG 저장
        svg_path = os.path.join(SVG_DIR, f"{name}.svg")
        with open(svg_path, "w") as f:
            f.write(svg_text)

        # PNG 변환 (1024×1024, 투명 배경)
        png_path = os.path.join(PNG_DIR, f"{name}.png")
        cairosvg.svg2png(
            bytestring=svg_text.encode("utf-8"),
            write_to=png_path,
            output_width=PNG_SIZE,
            output_height=PNG_SIZE,
        )
        print(f"saved  {name}.svg + {name}.png")

    print(f"\nDone. SVG: {SVG_DIR}\n       PNG: {PNG_DIR}")


if __name__ == "__main__":
    main()
